package day.two;

public class Operators {

	public static void main(String[] args)
	{
		    
		float a,b;//in o/p the values shows in float type
		a=20;
		b=9;
		
		System.out.println("The sum of a+b = "+(a+b));
		System.out.println("The subtract of a-b = "+(a-b));
		System.out.println("The multiply of a*b = "+(a*b));
		System.out.println("The division of a/b = "+(a/b));
		System.out.println("The remainder of a%b = "+(a%b));
     
	}

}
